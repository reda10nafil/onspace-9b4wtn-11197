// Helper function to check if product is dormant
export const isDormant = (product: Product): boolean => {
  const lastActivity = product.lastScannedAt || product.updatedAt;
  const daysSinceActivity = Math.floor(
    (Date.now() - new Date(lastActivity).getTime()) / (1000 * 60 * 60 * 24)
  );
  return daysSinceActivity >= 180;
};

// Helper function to check if product needs promotion suggestion
export const needsPromotion = (product: Product): boolean => {
  if (product.location !== 'magazzino') return false;
  const daysSinceCreation = Math.floor(
    (Date.now() - new Date(product.createdAt).getTime()) / (1000 * 60 * 60 * 24)
  );
  return daysSinceCreation >= 90;
};
